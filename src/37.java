public class MathCalculator {
    public static void main(String[] args) {
        // Example calculation: 10 + 5 = 15
        System.out.println("Result of 10 + 5 is " + (10 + 5));
        
        // Example calculation with floating-point numbers: 3.14 * 2 = 6.28
        double result = 3.14 * 2;
        System.out.println("Result of 3.14 * 2 is " + result);
    }
}
